import { NextRequest, NextResponse } from 'next/server'
import { auth } from '@/auth'
import { prisma } from '@/lib/prisma'
import bcrypt from 'bcryptjs'
import { logAuditAction, AuditActions, AuditResources } from '@/lib/auditLogger'

// Fun√ß√£o para extrair informa√ß√µes da requisi√ß√£o
function extractRequestInfo(req: NextRequest) {
  const ipAddress = req.headers.get('x-forwarded-for') ||
                   req.headers.get('x-real-ip') ||
                   'unknown'
  const userAgent = req.headers.get('user-agent') || 'unknown'
  
  return { ipAddress, userAgent }
}

export async function POST(req: NextRequest) {
  try {
    const session = await auth()

    if (!session?.user?.email || !session.user.email.includes('@mediz.com')) {
      return NextResponse.json({ error: 'N√£o autorizado' }, { status: 403 })
    }

    const { userId, newPassword } = await req.json()

    if (!userId) {
      return NextResponse.json({ error: 'ID do usu√°rio √© obrigat√≥rio' }, { status: 400 })
    }

    const userToReset = await prisma.user.findUnique({
      where: { id: userId }
    })

    if (!userToReset) {
      return NextResponse.json({ error: 'Usu√°rio n√£o encontrado' }, { status: 404 })
    }

    const hashedPassword = await bcrypt.hash(newPassword || 'mediz123', 10)

    await prisma.user.update({
      where: { id: userId },
      data: { passwordHash: hashedPassword }
    })

    const { ipAddress, userAgent } = extractRequestInfo(req)
    await logAuditAction({
      adminId: session.user.id,
      adminEmail: session.user.email,
      action: AuditActions.PASSWORD_RESET,
      resource: AuditResources.USER,
      resourceId: userId,
      details: {
        resetBy: session.user.email,
        targetUserEmail: userToReset.email,
        isDefaultPassword: !newPassword
      },
      ipAddress,
      userAgent
    })

    // üîí SEGURAN√áA: N√ÉO retornar senha na resposta JSON
    // A senha deve ser comunicada ao usu√°rio por outro canal seguro (email/WhatsApp)
    // NUNCA retornar senhas em respostas de API
    
    return NextResponse.json({
      success: true,
      message: `Senha do usu√°rio ${userToReset.email} resetada com sucesso.`
    })

  } catch (error) {
    console.error('Erro ao resetar senha:', error)
    return NextResponse.json({ error: 'Erro interno do servidor' }, { status: 500 })
  }
}