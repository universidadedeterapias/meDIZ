import { NextResponse } from 'next/server'
import { auth } from '@/auth'
import { cookies } from 'next/headers'

export async function GET() {
  try {
    // Usar o auth() importado de @/auth que j√° tem a configura√ß√£o
    const session = await auth()
    
    // üîí SEGURAN√áA: Endpoint de debug restrito apenas para admins
    if (!session?.user?.email?.includes('@mediz.com')) {
      return NextResponse.json(
        { error: 'N√£o autorizado. Apenas admins podem acessar este endpoint.' },
        { status: 403 }
      )
    }
    
    // Para debug, vamos verificar os cookies tamb√©m
    const cookiesList = await cookies()  // Adicionando await aqui
    const sessionTokenCookie = cookiesList.get('next-auth.session-token')
    
    return NextResponse.json({
      isAuthenticated: !!session,
      user: session?.user || null,
      isAdmin: session?.user?.email?.includes('@mediz.com') || false,
      cookies: {
        sessionToken: sessionTokenCookie ? 'Presente' : 'Ausente',
      },
      timestamp: new Date().toISOString(),
      serverInfo: {
        environment: process.env.NODE_ENV,
        nextAuthSecret: process.env.NEXTAUTH_SECRET ? 'Configurado' : 'N√£o configurado'
      }
    })
  } catch (error) {
    console.error('Erro ao verificar autentica√ß√£o:', error)
    return NextResponse.json(
      { 
        error: 'Erro ao verificar autentica√ß√£o', 
        errorMessage: error instanceof Error ? error.message : 'Erro desconhecido',
        timestamp: new Date().toISOString()
      },
      { status: 500 }
    )
  }
}